# Recover secret key a assuming k2 = c * k1

def recover_secret_key(max_multiplier: int = 1000):
    """
    We assume k2 = c * k1 for some small integer c in [1, max_multiplier].
    Using the DSA equations:
        s = k^{-1}(h + a r) mod q
    for both signatures, we derive a(c) and check if g^a ≡ beta (mod p).
    """
    inv_s1 = modinv(s1, q)
    if inv_s1 is None:
        raise ValueError("s1 has no inverse modulo q")

    for c in range(1, max_multiplier + 1):
        c_mod = c % q

        # A = s2 * c * s1^{-1}  (mod q)
        A = (s2 * c_mod * inv_s1) % q

        # a = [A*h1 - h2] * [r2 - A*r1]^{-1}   (mod q)
        num = (A * h1 - h2) % q
        den = (r2 - A * r1) % q
        inv_den = modinv(den, q)
        if inv_den is None:
            continue

        a = (num * inv_den) % q

        # Verify candidate a with the public key beta = g^a mod p
        if pow(g, a, p) == beta:
            print("Found multiplier c:", c)
            print("Recovered secret key a:", a)
            return a

    print("Failed to find a within multiplier range 1..", max_multiplier)
    return None

