from Crypto.Hash import SHAKE128

# Basic number theory helpers below

def egcd(a, b):
    x, y, u, v = 0, 1, 1, 0
    while a != 0:
        q, r = b // a, b % a
        m, n = x - u * q, y - v * q
        b, a, x, y, u, v = a, r, u, v, m, n
    return b, x, y

def modinv(a, m):
    if a < 0:
        a = a + m
    g, x, y = egcd(a, m)
    if g != 1:
        return None
    return x % m

def to_int(s: str) -> int:
    """Convert multi-line decimal string into a single integer."""
    return int("".join(line.strip() for line in s.strip().splitlines()))

